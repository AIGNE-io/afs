// ── AUP tree validation ──

interface ValidationResult {
  valid: boolean;
  checks: Record<string, boolean>;
}

function validateAupTree(tree: any): ValidationResult {
  const checks: Record<string, boolean> = {
    isObject: typeof tree === "object" && tree !== null,
    hasRootView: tree?.type === "view",
    hasId: typeof tree?.id === "string",
    hasChildren: Array.isArray(tree?.children) && tree.children.length >= 2,
    hasHeading: tree?.children?.some((c: any) => c.type === "text") ?? false,
    hasList: tree?.children?.some((c: any) => c.type === "list") ?? false,
    listHasSrc: (() => {
      const list = tree?.children?.find((c: any) => c.type === "list");
      return list?.src === "/data/todos";
    })(),
    listHasLayout: (() => {
      const list = tree?.children?.find((c: any) => c.type === "list");
      return list?.props?.layout === "list" || list?.layout === "list";
    })(),
    hasColumns: (() => {
      const list = tree?.children?.find((c: any) => c.type === "list");
      const cols = list?.props?.columns ?? list?.columns;
      return Array.isArray(cols) && cols.length === 4;
    })(),
  };
  return { valid: Object.values(checks).every(Boolean), checks };
}

