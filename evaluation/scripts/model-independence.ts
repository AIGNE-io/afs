interface ModelResult {
  model: string;
  vendor: string;
  mode: "simulated" | "live";
  valid: boolean;
  outputTokens: number;
  outputBytes: number;
  latencyMs: number;
  checks: Record<string, boolean>;
  structuralNotes: string[];
}

async function runExperiment(): Promise<ModelResult[]> {
  const results: ModelResult[] = [];

  if (isLive) {
    console.log("Mode: LIVE API calls (requires API keys)\n");

    // Anthropic models
    const anthropicModels = [
      { name: "claude-haiku", id: "claude-haiku-4-5-20251001" },
      { name: "claude-sonnet", id: "claude-sonnet-4-5-20250514" },
      { name: "claude-opus", id: "claude-opus-4-5-20250514" },
    ];

    for (const m of anthropicModels) {
      try {
        console.log(`  Calling ${m.name}...`);
        const { output, latencyMs } = await callAnthropicAPI(m.id);
        const validation = validateAupTree(output);
        const json = JSON.stringify(output);
        results.push({
          model: m.name,
          vendor: "Anthropic",
          mode: "live",
          valid: validation.valid,
          outputTokens: Math.ceil(json.length / 4),
          outputBytes: new TextEncoder().encode(json).byteLength,
          latencyMs,
          checks: validation.checks,
          structuralNotes: analyzeStructure(output),
        });
      } catch (err) {
        console.log(`  ${m.name}: ${(err as Error).message}`);
      }
    }

    // OpenAI models
    if (process.env.OPENAI_API_KEY) {
      const openaiModels = [
        { name: "gpt-4o-mini", id: "gpt-4o-mini" },
        { name: "gpt-4o", id: "gpt-4o" },
      ];
      for (const m of openaiModels) {
        try {
          console.log(`  Calling ${m.name}...`);
          const { output, latencyMs } = await callOpenAIAPI(m.id);
          const validation = validateAupTree(output);
          const json = JSON.stringify(output);
          results.push({
            model: m.name,
            vendor: "OpenAI",
            mode: "live",
            valid: validation.valid,
            outputTokens: Math.ceil(json.length / 4),
            outputBytes: new TextEncoder().encode(json).byteLength,
            latencyMs,
            checks: validation.checks,
            structuralNotes: analyzeStructure(output),
          });
        } catch (err) {
          console.log(`  ${m.name}: ${(err as Error).message}`);
        }
      }
    }
  } else {
    console.log("Mode: SIMULATED (pre-collected outputs from Claude Haiku/Sonnet/Opus)\n");
    console.log(
      "  To run with real API calls: bun evaluation/scripts/model-independence.ts --live\n",
    );

    for (const [model, output] of Object.entries(SIMULATED_OUTPUTS)) {
      const validation = validateAupTree(output);
      const json = JSON.stringify(output);
      results.push({
        model,
        vendor: "Anthropic",
        mode: "simulated",
        valid: validation.valid,
        outputTokens: Math.ceil(json.length / 4),
        outputBytes: new TextEncoder().encode(json).byteLength,
        latencyMs: 0,
        checks: validation.checks,
        structuralNotes: analyzeStructure(output),
      });
    }
  }

  return results;
}

function analyzeStructure(tree: any): string[] {
  const notes: string[] = [];
  const heading = tree?.children?.find((c: any) => c.type === "text");
  if (heading) {
    if (heading.props) notes.push("props in props object");
    else notes.push("props inline (non-standard but renderable)");
  }
  const list = tree?.children?.find((c: any) => c.type === "list");
  if (list) {
    const cols = list.props?.columns ?? list.columns;
    if (cols?.[0]?.key) notes.push('columns use "key" field');
    if (cols?.[0]?.field) notes.push('columns use "field" field');
  }
  if (!tree.id) notes.push("missing root id");
  return notes;
}

// ── Run and report ──

const results = await runExperiment();

